            <h4 style="color: deepskyblue">Introduction </h4>

            <p>
                Bachelors of Science in Computer Science and Information Technology (B.Sc. CSIT) is a four years course program of Tribhuvan University designed to provide students with all sorts of knowledge in the field of Computing and Information Technology.</p>
                <p>The program involves, in addition to conventional lectures, a great  deal of practical and project works. The program develops the underlying principles of both Computer Science and Information Technology and shows how these principles can be applied into the real world problems. This program develops the skills that are essential for both computer professionals and IT specialists.</p>
                <p>The design and implementation of B.Sc. CSIT course offers new challenges when compared to the traditional computing environment. The recent emergence of global business, new technologies for data processing and data communication / networking environment, equip specialized science graduates to focus on professional careers in Information Technology. This course provides the students with adequate theoretical and practical knowledge which will enable them to effectively participate in solving the complex problem of the IT industry.

            </p>
            <h4>Mission of the Course</h4>
            <p>
                The mission of the B.Sc. CSIT course is to prepare the students to pursue career advancement in the field of information technology. At the completion of this degree, a student will be able to design the real world e-media products, developing theories to evolve new technologies or creating technical solutions to hardware and software problems, depending on the chosen area and electives.</p>

